package software.testing;

import java.util.Scanner;

public class PalindromeNumber {

	public static void main(String[] args) {
		
		
	for(int r=1;r<=7;r=r+2) {
		for(int c=1;c<=r;c++) {
			System.out.print(r);
			
			System.out.print(" ");
		}
		System.out.println("");
	}
	for(int r=5;r>=1;r--) {
		for(int c=1;c<=r;c++) {
			System.out.print("*");
			System.out.print(" ");
		}
		System.out.println("");
	}
		
		
	}

}

